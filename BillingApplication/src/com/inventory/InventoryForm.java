    private void jButton_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable_Inventory.getModel();
        String code = jTextField_Code.getText();
        String details = jTextField_Details.getText();
        String qty = jTextField_Quantity.getText();
        String hsn = jTextField_HSN.getText();
        String tax =jTextField_Tax.getText();
       
        if(!validateBillPage()){
            jLabel_NotificationMessage.setText("Please provide ItemCode,Details,Company & Quantity");
        }
        
        InventoryDAO inventory = new InventoryDAO();
        if(inventory.getDetailsFromItemNo(code) == null){
            InventoryModel detail = new InventoryModel();
            detail.setItem_no(jTextField_Code.getText());
            detail.setDetails(jTextField_Details.getText());
            detail.setCompany(jTextField_Company.getText());
            detail.setQuantity(Double.parseDouble(jTextField_Quantity.getText()));
            
            if(jTextField_Tax.getText().equals(null) || jTextField_Tax.getText().isEmpty()){
                 detail.setTax(0.0);
            }else{
                 detail.setTax(Double.parseDouble(jTextField_Tax.getText()));
            }
           
            if(jTextField_HSN.getText().equals(null) || jTextField_HSN.getText().isEmpty()){
                detail.setHsnID(0);
            }else{
                detail.setHsnID(Integer.parseInt(jTextField_HSN.getText()));
            }
           
            detail.setCostPrice(0);
            detail.setSalePrice(0);
            detail.setLocation(" ");
            detail.setItemNotes("");
            detail.setRegister_date(inventory.getCurrentDatetime());
            
            inventory.insertIntoInventory(detail);
            jLabel_NotificationMessage.setText("Item Added to Inventory" +code);
            loadTableWithData();
            
        }else{
            jLabel_NotificationMessage.setText("ItemCode Already Exist");
            jTextField_Details.setText(details);
            jTextField_Quantity.setText(qty);
            jTextField_HSN.setText(hsn);
            jTextField_Tax.setText(tax);
        }
        
    }//GEN-LAST:event_jButton_AddActionPerformed

    public void removeTableEntries(){
       DefaultTableModel model = (DefaultTableModel)jTable_Inventory.getModel();

        while(model.getRowCount() > 0){
            for(int i = 0 ; i < model.getRowCount();i++){
                 model.removeRow(i);
            }
        }
    }
    
