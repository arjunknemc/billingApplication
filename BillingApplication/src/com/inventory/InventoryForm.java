    private void loadTableWithData(){
        InventoryDAO inventory = new InventoryDAO();
        existingInventory = inventory.getAllDetails();
        removeTableEntries();
        DefaultTableModel model = (DefaultTableModel) jTable_Inventory.getModel();
        for(int i=0 ; i< existingInventory.size() ; i++){
            InventoryModel row = existingInventory.get(i);
            model.addRow(new Object[]{row.getItem_no(),row.getDetails(), row.getQuantity(), row.getHsnID(), row.getTax(), row.getCompany()});
        }
    }

    public boolean validateBillPage() {
        if (jTextField_Code.getText().isEmpty()
                || jTextField_Details.getText().isEmpty() || jTextField_Company.getText().isEmpty() 
                || jTextField_Quantity.getText().isEmpty()) {
            return false;
        } else {
            return true;
        }
    }
