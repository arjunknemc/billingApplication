    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        // TODO add your handling code here:
        
        if(!validateBillPage()){
            jLabel_NotificationMessage.setText("Please provide ItemCode,Details,Company & Quantity");
        }
        
        InventoryDAO inventory = new InventoryDAO();

        InventoryModel detail = new InventoryModel();
        detail.setItem_no(jTextField_Code.getText());
        detail.setDetails(jTextField_Details.getText());
        detail.setCompany(jTextField_Company.getText());
        detail.setQuantity(Double.parseDouble(jTextField_Quantity.getText()));

        if (jTextField_Tax.getText().equals(null) || jTextField_Tax.getText().isEmpty()) {
            detail.setTax(0.0);
        } else {
            detail.setTax(Double.parseDouble(jTextField_Tax.getText()));
        }

        if (jTextField_HSN.getText().equals(null) || jTextField_HSN.getText().isEmpty()) {
            detail.setHsnID(0);
        } else {
            detail.setHsnID(Integer.parseInt(jTextField_HSN.getText()));
        }

        detail.setCostPrice(0);
        detail.setSalePrice(0);
        detail.setLocation(" ");
        detail.setItemNotes("");
        inventory.updateInventory(detail);
        jLabel_NotificationMessage.setText("Item updated to Inventory");
        loadTableWithData();
        makeFieldsEmpty();
    }//GEN-LAST:event_jButton_UpdateActionPerformed

