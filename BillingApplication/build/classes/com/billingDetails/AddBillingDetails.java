    public void updateInventory(Bill bill, String shopName){
        ArrayList<BillDetails> details = bill.getDetails();
        for(int i=0; i<details.size() ; i++){
           String itemCode = details.get(i).getItemCode();
           InventoryDAO inventory_connection = new InventoryDAO();
           InventoryModel itemData = inventory_connection.getDetailsFromItemNo(itemCode,shopName);
           if(itemData != null){
                double dbQuantity = itemData.getQuantity();
                double userQuanity = details.get(i).getQty();
                double updateValue = dbQuantity - userQuanity;
                if(updateValue < 0){
                    updateValue = 0;
                }
                inventory_connection.updateQuantity(itemCode, updateValue);
            }
        }
       
    }
    
    private void jButton_ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConfirmActionPerformed
        // show shop popup for billing
        if (jTable_Items.getModel().getRowCount() == 0) {
            jLabel1_Message.setText("Empty Bill Cannot be Generated, Please add Items using Add Option");
        } else {
            AdditionalBillingDetails additional = new AdditionalBillingDetails();
            additional.setBillType(billType);
            additional.setDay(jComboBox_DD.getSelectedIndex()+1);
            additional.setMonth(jComboBox_MM.getSelectedIndex()+1);
            
            ProcessConfirmAction action = new ProcessConfirmAction();
            ArrayList<ShopDetails> shopDetails = action.getShopDetails();
            ShopDetails confirmedShopDetails = new ShopDetails();

            if (shopDetails.size() == 1) {
                confirmedShopDetails = shopDetails.get(0);
            } else {
                String selectedShopName = action.confirmAction(rootPane, shopDetails);

                for (int i = 0; i < shopDetails.size(); i++) {
                    if (shopDetails.get(i).getName().equals(selectedShopName)) {
                        confirmedShopDetails = shopDetails.get(i);
                    }
                }
            }

            ProcessBillingDetails details = new ProcessBillingDetails();
            Bill bill = details.process(jTable_Items, additional, confirmedShopDetails);
            InvoiceDAO insertInvoice = new InvoiceDAO();

            if (billType.equals("invoice")) {
                insertInvoice.insertInvoice(bill, confirmedShopDetails);
            } else {
                insertInvoice.updateInvoiceId(bill.getInvoiceId(), billType, confirmedShopDetails);
            }
            updateInventory(bill, confirmedShopDetails.getName());

            CreateSimpleWorkingHtml html = new CreateSimpleWorkingHtml();
            if (jTextField_CustName.getText().isEmpty()) {
                html.setCustomerName("");
                html.setCustomerDetails("");
                html.setAdditionalDetails("");
            } else {
                html.setCustomerName(jTextField_CustName.getText());
                html.setCustomerDetails(jTextField_Cust_Address.getText());
                html.setAdditionalDetails(jTextField_AddDetails.getText());
            }
            String file = html.create(confirmedShopDetails, bill);
            File htmlFile = new File(file);
            try {
                Desktop.getDesktop().browse(htmlFile.toURI());
                
                PrinterJob pj = PrinterJob.getPrinterJob();
               // pj.printDialog();
                PageFormat pf = pj.defaultPage();
                Paper paper = new Paper();
                paper.setSize(5.8, 8.3);
                pf.setPaper(paper);
              //  pj.pageDialog(pf);
                // pj.print();
            } catch (IOException e) {

            }

            JOptionPane.showMessageDialog(rootPane, "Bill Generated Successfully. BillNo: " + bill.getInvoiceId());
            removeTableEntries();
            jTextField_Total.setText(null);
            jTextField_CustName.setText(null);
            jTextField_Cust_Address.setText(null);
            jTextField_AddDetails.setText(null);
            count = 1;
            loadComboBoxWithValues();

        }
    }//GEN-LAST:event_jButton_ConfirmActionPerformed

